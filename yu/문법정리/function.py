# itertools 모듈 : 반복자를 쉽게 다루도록 도와주는 함수들의 모음 

# -combinations(iterable, r) : iterable에서 r개를 뽑아 순서 없이 조합을 만듦
# -permutations(iterable, r) : iterable에서 r개를 뽑아 순서를 고려한 모든 순열 생성
# -product(iterable, repeat=n) iterable의 모든 가능한 n개 곱집합 생성(중복 허용)
# -combinations_with_replacement(iterable, r) : combination이랑 비슷하지만 중복 선택 가능

# collections 모듈 : 자료구조를 더 편하게 쓰도록 돕는 클래스들이 ㅁ낳음

# -Counter : 리스트나 문자열 등에서 각 요소가 몇 번 등장 하는지 세어줌
# -defaultdict : 딕셔너리를 쓸 때 키가 없으면 자동으로 기본값을 만들어 줌
# -deque : 큐, 덱 자료구조 양쪽에서 빠른 추가/ 삭제 가능

